<template>
  <div id="app">
    <router-view v-if="isFresh"></router-view>
  </div>
</template>

<script>
  export default {
    name:'app',
    provide(){
      return {
        reload: this.reload
      }
    },
    data(){
      return{
        isFresh: true
      }
    },
    methods:{
      reload(){
        this.isFresh = false;
        this.$nextTick(()=>{
          this.isFresh = true;
        });
      }
    }
  }
</script>
